    from server import mcp as nexus_mcp  # noqa: E402

    rest_app = create_rest_app()
    discover_tools_from_mcp(nexus_mcp)

    log.info(f"Standalone REST gateway: {len(_TOOL_REGISTRY)} tools registered")
    uvicorn.run(rest_app, host="0.0.0.0", port=8081)
